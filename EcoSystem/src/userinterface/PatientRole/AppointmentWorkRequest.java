      public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_Patient.getModel();

        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
            if ((request.getStatus().equals("Assigned To Doctor")) || (request.getStatus().equals("Pending"))) {
                Object[] row = new Object[6];
                row[0] = request;
                row[1] = request.getAppointmentID();
                row[2] = request.getRequestDate();
                row[3] = request.getTime();
                row[4] = request.getStatus();
                row[5]= request.getPatientID();
                model.addRow(row);
            }

        }
    
        
    }

