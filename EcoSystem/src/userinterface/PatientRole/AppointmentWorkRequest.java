    private void btnAssignmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignmeActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_Patient.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please make a selection");
            return;
        }
        if (count == 0) {
            WorkRequest request = (WorkRequest) tbl_Patient.getValueAt(selectedRow, 0);
            count++;
            request.setReceiver(userAccount);
            request.setStatus("Pending");
            populateTable();
            JOptionPane.showMessageDialog(null, "Assigned");

        } else {
            JOptionPane.showMessageDialog(null, "Cannot Assign More Than One Patient");
        }
    }//GEN-LAST:event_btnAssignmeActionPerformed


